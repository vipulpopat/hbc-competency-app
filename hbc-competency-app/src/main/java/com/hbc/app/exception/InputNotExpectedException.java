package com.hbc.app.exception;

@SuppressWarnings("serial")
public class InputNotExpectedException extends Exception {

	public InputNotExpectedException() {
		super();
	}

	public InputNotExpectedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public InputNotExpectedException(String arg0) {
		super(arg0);
	}

}
